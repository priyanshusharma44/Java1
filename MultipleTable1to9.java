//Write a Java program to generate multiplication tables of 1to9
public class MultipleTable1To9 {
    public static void main(String[] args) {
            int n=1;
            while(n<9)
            {
            
            for(int i=1;i<=10;i++)
            {
            System.out.println(i*n);
            System.out.println();
            
            }
            n++;
    }
}

}
