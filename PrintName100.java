//Write a Java program to print your name 100 times.

public class PrintName100 {
    public static void main(String[] args) {
           
      String name="Priyanshusharma";
      for(int i=1;i<=100;i++)
        {
            System.out.println(i+": "+name); 
            System.out.println();
        }
        

    }
}